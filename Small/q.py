import turtle

t = turtle.Turtle()
t.speed(3)

t.pensize(6)

t.penup()
t.goto(-50, 50)
t.setheading(90)
t.pendown()

t.left(90)

t.forward(40)
t.circle(-30, 180)
t.forward(40)
t.left(90)
t.forward(5)
t.right(180)
t.forward(130)

t.left(120)
t.forward(50)



turtle.done()
