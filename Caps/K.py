import turtle
t = turtle.Turtle()
t.speed(3)

t.pensize(6)

t.penup()
t.goto(-100, 0)
t.setheading(90)  
t.pendown()
t.forward(150)
t.right(180)
t.penup()
t.forward(75)
t.left(90)
t.pendown()
t.left(45)
t.forward(90)
t.right(180)
t.penup()
t.forward(90)
t.left(90)
t.pendown()
t.forward(90)




turtle.done()